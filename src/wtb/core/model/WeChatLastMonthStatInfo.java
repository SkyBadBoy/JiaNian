package wtb.core.model;

public class WeChatLastMonthStatInfo {
	public WeChatLastMonthStatInfo() {
		super();
	}
	private int Year;
	private int Month;
	private String Days;
	private int Hour;
	private int Count;
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public String getDays() {
		return Days;
	}
	public void setDays(String days) {
		Days = days;
	}
	public int getCount() {
		return Count;
	}
	public void setCount(int count) {
		Count = count;
	}
	public int getHour() {
		return Hour;
	}
	public void setHour(int hour) {
		Hour = hour;
	}
	
	
	
	
}
